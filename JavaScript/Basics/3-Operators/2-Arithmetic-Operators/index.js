//Arithmetic Operators

let x = 10;
let y = 3;

console.log(x + y);     //adding x and y
console.log(x - y);     // subtracting x with y
console.log(x * y);     // multiply x and y
console.log(x / y);     // dividing x with y
console.log(x % y);     
console.log(x ** y);    // x to the power of y

//Increment (++)
console.log(++x);    // x will be incemented by 1 first and then we will see that on the console. - (o/p 11)

console.log(x++);   // we will see x on the console first and then the value x will be incremented by 1. - (o/p 11)
console.log(x);     //o/p 12 

//Decrement (--)
console.log(--y);   // y will be decemented by 1 first and then we will see that on the console. - (o/p 2)

console.log(y--);   // we will see y on the console first and then the value x will be decremented by 1. - (o/p 2)
console.log(y);     //o/p 1