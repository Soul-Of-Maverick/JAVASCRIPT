//Comparison Operators
//comparison operator is a boolean . it's true or false

let x = 1;

console.log(x > 0);     // o/p - true 
console.log(x >= 1);     // o/p - true
console.log(x < 1);     // o/p - false
console.log(x <= 1);     // o/p - true

console.log(x === 1);     // o/p - true
console.log(x !== 1);     // o/p - false