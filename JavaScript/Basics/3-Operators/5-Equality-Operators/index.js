//Equality Operators

// Strict Equality
// === ensures that both uprand are of the same type and of the same value
console.log(1 === 1);   // o/p true
console.log('1' === 1);   // o/p false

// Lose Equality
// ==  Lose Equality doesn't care about the type matching, if the types dont match, it will convert the type of what we have on the right side to match what we have on the left side, and only check values are equal
console.log(1 == 1);   // o/p true
console.log('1' == 1);   // o/p true
console.log(true == 1);   // o/p true