// Function Declarations vs. Expressions

// JS two ways to define function
// - Function Declaration
// - Function Expression

// Function Declaration
function walk() {
    console.log('walk');
}

// Annoymous Function Expression
const run = function() {
    console.log('run');
};

// Nammed Function Expression
const run1 = function run() {
    console.log('run1');
};

